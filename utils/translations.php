<?php
function translateMonth($month)
{
    switch ($month) {
        case 'January':
            echo "Enero";
            break;
        case 'February':
            echo "Febrero";
            break;
        case 'March':
            echo "Marzo";
            break;
        case 'April':
            echo "Abril";
            break;
        case 'May':
            echo "Mayo";
            break;
        case 'June':
            echo "Junio";
            break;
        case 'July':
            echo "Julio";
            break;
        case 'August':
            echo "Agosto";
            break;
        case 'September':
            echo "Septiembre";
            break;
        case 'Octuber':
            echo "Octubre";
            break;
        case 'November':
            echo "Noviembre";
            break;
        case 'December':
            echo "Diciembre";
            break;
    }
}
